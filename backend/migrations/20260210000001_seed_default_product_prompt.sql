- 保険商品の購入フロー。
- 証拠提出フォーム（画像・PDF・URL）。
- 請求ステータス追跡（受付・審査中・追加提出・承認/却下・支払い完了）。
- 通知とリマインド機能。

## 制約とタイムライン
- 個人情報と証跡データを安全に扱うことが必須。
- 監査対応のため、提出履歴と更新履歴を保持する。
- まずはMVPとして主要な請求フローを優先し、段階的に機能拡張する。

## 差別化ポイントと補足
- 書類提出のガイドをステップ化し、ユーザーの迷いを減らす。
- 不足証跡を自動で指摘し、再提出を最小化する。
