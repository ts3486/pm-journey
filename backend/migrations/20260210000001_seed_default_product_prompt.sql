## 対象ユーザーと課題
- 対象は、個人契約者と小規模事業者。
- 保険金請求時に必要書類が分かりづらく、提出漏れや差し戻しが発生しやすい。
- 請求ステータスが見えず、不安や問い合わせ増加につながっている。

## 目標と成功条件
- ユーザーが迷わず請求を完了できる体験を提供する。
- 提出から支払いまでのリードタイムを短縮する。
- 成功条件: 請求完了率の改善、差し戻し率の低下、初回支払いまでの日数短縮。

